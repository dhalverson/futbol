class Games

  def initialize(args)
    @game_id = args[:game_id]
    @season = args[:season]
    @type = args[:type]
    @date_time = args[:date_time]
    @away_team_id = args[:away_team_id]
    @home_team_id = args[:home_team_id]
    @away_goals = args[:away_goals]
    @home_goals = args[:home_goals]
  end




end
